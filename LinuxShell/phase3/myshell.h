/* $begin parseline */
/* parseline - Parse the command line and build the argv array */
int parseline(char *buf, char **argv) 
{
    char *delim;         /* Points to first space delimiter */
    int argc;            /* Number of args */
    int bg;              /* Background job? */

    buf[strlen(buf)-1] = ' ';  /* Replace trailing '\n' with space */
    while (*buf && (*buf == ' ')) /* Ignore leading spaces */
	    buf++;

    /* Build the argv list */
    argc = 0;
    while ((delim = strchr(buf, ' '))) 
    {
	    argv[argc++] = buf;

        char *temp_buf = buf; 
        char *quote_start = buf; /* 여는 따옴표 */
        char *quote_end = buf; /* 닫는 따옴표 */
        int is_quote = 0; /* 따옴표가 있는지 여부 */
        
        while (*temp_buf)
        {
            if(*temp_buf == ' ')
                break;
            if(*temp_buf == '\'') 
            {
                is_quote = 1;
                quote_start = temp_buf++;
                quote_end = strchr(temp_buf, '\'');
                temp_buf = quote_end;
                delim = strchr(temp_buf, ' ');
                break;
            }
            else if(*temp_buf == '\"') 
            {
                is_quote = 1;
                quote_start = temp_buf++;
                quote_end = strchr(temp_buf, '\"');
                temp_buf = quote_end;
                delim = strchr(temp_buf, ' ');
                break;
            }
            temp_buf++;
        }
        

    	*delim = '\0';
        if(is_quote) /* 따옴표가 있을 경우 */
        {
            temp_buf = buf;
            while(temp_buf <= delim)
            {
                if(buf > delim)
                {
                    *temp_buf = ' ';
                    temp_buf++;
                }
                else if(buf != quote_start && buf != quote_end)
                {
                    *temp_buf = *buf;
                    temp_buf++;
                }
                buf++;
            }
        }
    	buf = delim + 1;
    	while (*buf && (*buf == ' ')) /* Ignore spaces */
            buf++;
    }
    argv[argc] = NULL;
    
    if (argc == 0)  /* Ignore blank line */
    	return 1;

    /* Should the job run in the background? */
    if ((bg = (*argv[argc-1] == '&')) != 0)
        argv[--argc] = NULL;

    return bg;
}
/* $end parseline */

int pipe_execve(char **pipe_cmd_list, int pipe_cmd_cnt)
{
    char *argv[MAXARGS];
    char buf[MAXLINE];
    pid_t pid;
    int fd[MAXARGS][2];

    for(int i = 0; i < pipe_cmd_cnt; i++)
    {
        char filename[MAXARGS] = "/bin/";
        
        strcpy(buf, pipe_cmd_list[i]); /* pipe가 없을 때와 동일하게 초기값 설정 */ 
        parseline(buf,argv); 
        strcat(filename, argv[0]);

        if(pipe(fd[i]))
            exit(-1);

        if((pid = Fork()) == 0)
        {
            if(i == 0) /* 첫 명령어 read X  */
            {
                close(fd[i][0]);
                dup2(fd[i][1], STDOUT_FILENO);
                close(fd[i][1]);
                Execve(filename, argv, environ);
            }
            else if(i == pipe_cmd_cnt - 1) /* 마지막 명령어 write X */
            {
                close(fd[i-1][1]);
                dup2(fd[i-1][0], STDIN_FILENO);
                close(fd[i-1][0]);
                Execve(filename, argv, environ);
            }
            else /* 그 외 read O write O */
            {
                dup2(fd[i-1][0], STDIN_FILENO); 
                dup2(fd[i][1], STDOUT_FILENO);
                close(fd[i-1][0]);
                close(fd[i][1]);
                Execve(filename, argv, environ);
            }
        }
        close(fd[i][1]); /* 이후 fd[i][0]은 stdin으로 사용되므로 close X */
       
    }
    exit(0);
}
